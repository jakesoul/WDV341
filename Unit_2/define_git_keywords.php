        <h1>Git Terms</h1>
        
        <ol>
            <li>Version control - tracks changes to files and allows multiple people to collaborate on a project without overwriting each other's work. e.g. Git is a popular version control software</li>
            <li>Add - a command that stages changes to be committed.</li>
            <li>Commit - creates a snapshot of the changes within the working directory</li>
            <li>Push - sends commits to a remote repository, like a GitHub repository.</li>
            <li>Pull - retrieves commits from a remote repository</li>
            <li>Clone - creates a local copy of a remote repository on your computer.</li>
        </ol>

